package game_project;


public class CharacterTemplate {
	int ID;
	int HP;
	int ATK;
	int DEF;
	int SPATK;
	int SPDEF;
	int SPD;
	public CharacterTemplate(int id, int hP, int aTK, int dEF, int sPATK, int sPDEF, int sPD) {
		super();
		ID = id;
		HP = hP;
		ATK = aTK;
		DEF = dEF;
		SPATK = sPATK;
		SPDEF = sPDEF;
		SPD = sPD;
	}
	
}
